#include <conio.h>
#include <iostream>
using namespace std;

//ヒーロークラス定義
class Hero {	
	int hp = 0;				//HP（0）

public:
	//HP加算メソッド
	void addHp(){
		int cnt = 0;		//ループカウンタ

		//ループカウンタとHPの加算を５回繰返す
		do
		{
			cout << "HPを加算　" << cnt+1 << "回目" << endl;
			hp += 100;

			cnt++;			//ループカウンタを１増やす

		}while(cnt < 5);
	}

	//HP取得メソッド
	int getHp(){ return hp; }
};
////////////////////////////////////////////////////////////
int main()
{
	Hero hero;		//ヒーローオブジェクト宣言

	hero.addHp();		//HP加算メソッドの呼び出し

	cout << endl << "HP=" << hero.getHp() << endl;

	_getch();
	return 0;
}


